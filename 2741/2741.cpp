#include <iostream>
using namespace std;

int main(void)
{
    ios::sync_with_stdio(false);
    cin.tie(NULL);

    int n;

    cin >> n;
    for (int i = 1;i <= n;i++)
    {
        cout << i << "\n";
    }

    return 0;
}